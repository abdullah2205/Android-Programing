package com.kuuhaku.tuxapps;

public class App
{
    private String nama, deskripsi, versi, tipe_paket, arsitektur, rilis, tgl_rilis, ukuran;
    private int ikon;

    public String getNama() {
        return nama;
    }

    public void setNama(String Nama) {
        this.nama = Nama;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String Deskripsi) {
        this.deskripsi = Deskripsi;
    }

    public String getVersi() {
        return versi;
    }

    public void setVersi(String Versi) {
        this.versi = Versi;
    }

    public String getTipe_paket() {
        return tipe_paket;
    }

    public void setTipe_paket(String Tipe_paket) {
        this.tipe_paket = Tipe_paket;
    }

    public String getArsitektur() {
        return arsitektur;
    }

    public void setArsitektur(String Arsitektur) {
        this.arsitektur = Arsitektur;
    }

    public String getRilis() {
        return rilis;
    }

    public void setRilis(String Rilis) {
        this.rilis = Rilis;
    }

    public String getTgl_rilis() {
        return tgl_rilis;
    }

    public void setTgl_rilis(String Tgl_rilis) {
        this.tgl_rilis = Tgl_rilis;
    }

    public String getUkuran() {
        return ukuran;
    }

    public void setUkuran(String Ukuran) {
        this.ukuran = Ukuran;
    }

    public int getIkon() {
        return ikon;
    }

    public void setIkon(int Ikon) {
        this.ikon = Ikon;
    }
}
